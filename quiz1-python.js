var quizApp = function () {
    this.score = 0;
    this.qno = 1;
    this.currentque = 0;
    var totalque = quiz.Python.length; // Corrected property name here

    this.displayQuiz = function (cque) {
        this.currentque = cque;
        if (this.currentque < totalque) {
            $("#tque").html(totalque);
            $("#previous").attr("disabled", false);
            $("#next").attr("disabled", false);
            $("#qid").html(quiz.Python[this.currentque].id + '.'); // Corrected property name here
            $("#question").html(quiz.Python[this.currentque].question); // Corrected property name here
            $("#question-options").html("");
            for (var key in quiz.Python[this.currentque].options[0]) {
                if (quiz.Python[this.currentque].options[0].hasOwnProperty(key)) {
                    $("#question-options").append(
                        "<div class='form-check option-block'>" +
                        "<label class='form-check-label'>" +
                        "<input type='radio' class='form-check-input' name='option' id='q" + key + "' value='" + quiz.Python[this.currentque].options[0][key] + "'><span id='optionval'>" +
                        quiz.Python[this.currentque].options[0][key] +
                        "</span></label>"
                    );
                }
            }
        }
        if (this.currentque <= 0) {
            $("#previous").attr("disabled", true);
        }
        if (this.currentque >= totalque) {
            $('#next').attr('disabled', true);
            for (var i = 0; i < totalque; i++) {
                this.score = this.score + quiz.Python[i].score; // Corrected property name here
            }
            return this.showResult(this.score);
        }
    }

    this.showResult = function (scr) {
        $("#result").addClass('result');
        $("#result").html("<h1 class='res-header'>Total Score: &nbsp;" + scr + '/' + totalque + "</h1>");
        for (var j = 0; j < totalque; j++) {
            var res;
            if (quiz.Python[j].score == 0) { // Corrected property name here
                res = '<span class="wrong">' + quiz.Python[j].score + '</span><i class="fa fa-remove c-wrong"></i>'; // Corrected property name here
            } else {
                res = '<span class="correct">' + quiz.Python[j].score + '</span><i class="fa fa-check c-correct"></i>'; // Corrected property name here
            }
            $("#result").append(
                '<div class="result-question"><span>Q ' + quiz.Python[j].id + '</span> &nbsp;' + quiz.Python[j].question + '</div>' + // Corrected property name here
                '<div><b>Correct answer:</b> &nbsp;' + quiz.Python[j].answer + '</div>' + // Corrected property name here
                '<div class="last-row"><b>Score:</b> &nbsp;' + res +
                '</div>'
            );
        }
    }

    this.checkAnswer = function (option) {
        var answer = quiz.Python[this.currentque].answer; // Corrected property name here
        option = option.replace(/</g, "&lt;");
        option = option.replace(/>/g, "&gt;");
        option = option.replace(/"/g, "&quot;");
        if (option == quiz.Python[this.currentque].answer) { // Corrected property name here
            if (quiz.Python[this.currentque].score == "") { // Corrected property name here
                quiz.Python[this.currentque].score = 1; // Corrected property name here
                quiz.Python[this.currentque].status = "correct"; // Corrected property name here
            }
        } else {
            quiz.Python[this.currentque].status = "wrong"; // Corrected property name here
        }
    }

    this.changeQuestion = function (cque) {
        this.currentque = this.currentque + cque;
        this.displayQuiz(this.currentque);
    }
}

var jsq = new quizApp();
var selectedopt;

$(document).ready(function () {
    jsq.displayQuiz(0);
    $('#question-options').on('change', 'input[type=radio][name=option]', function (e) {
        $(this).prop("checked", true);
        selectedopt = $(this).val();
    });
});

$('#next').click(function (e) {
    e.preventDefault();
    if (selectedopt) {
        jsq.checkAnswer(selectedopt);
    }
    jsq.changeQuestion(1);
});

$('#previous').click(function (e) {
    e.preventDefault();
    if (selectedopt) {
        jsq.checkAnswer(selectedopt);
    }
    jsq.changeQuestion(-1);
});
